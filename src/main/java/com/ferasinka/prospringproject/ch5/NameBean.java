package com.ferasinka.prospringproject.ch5;

public class NameBean {
	public void foo() {
		System.out.println("foo()");
	}
	
	public void foo(int x) {
		System.out.println("foo(int) " + x);
	}
	
	public void bar() {
		System.out.println("bar()");
	}
	
	public void yup() {
		System.out.println("yup()");
	}
}
